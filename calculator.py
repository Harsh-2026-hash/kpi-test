def add(a, b):
    return a + b

def subtract(a, b):
    return a - b

def multiply(a, b):
    return a * b

def divide(a,b):
    if b==0:
        raise ValueError("Cannot divide by zero b must be non zero")
    return a/b

def modulo(a,b):
    return a%b

def absolute(a):
    return abs(a)
